// Only works in page components

// Next.js uses a server --> user makes request to server --> server delivers page --> cuts out a ton of application weight because server only sends specific page rather than entire app
export async function getStaticProps() {
  const exploreData = await fetch(`https://links.papareact.com/pyp`).then(
    (res) => res.json()
  );

  const cardsData = await fetch(`https://links.papareact.com/zp1`).then((res) =>
    res.json()
  );
  // Passes to functional component in browser
  return {
    props: {
      exploreData,
      cardsData,
    },
  };
}
